"""
Single-experiment runner for the Synthetic HR-MDP benchmark.

For one (p_switch, seed) combination:
  1. Fit a Gaussian HMM on synthetic returns generated by the env.
  2. Train baseline / hard / soft A2C agents on the training env.
  3. Evaluate ALL four policies (3 RL variants + Oracle) on the SAME test
     trajectory — same env seed → same regime sequence → same realised
     returns.  Differences between agents come purely from action choices.
  4. Compute step-wise regret = log_wealth_oracle − log_wealth_agent.
  5. Save curves, training histories, and a per-seed figure.

Stand-alone invocation runs one experiment; `sweep.py` loops this across
multiple p_switch values × seeds to build Figure 1.
"""

