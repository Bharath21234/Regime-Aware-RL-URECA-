def _save_result(result: dict, path: str):
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    # histories are lists of floats (Python ints/floats), already serialisable
    with open(path, "w") as f:
        json.dump(result, f, indent=2)


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--p_switch", type=float, default=0.05)
    parser.add_argument("--seed",     type=int,   default=0)
    parser.add_argument("--epochs",   type=int,   default=200)
    parser.add_argument("--train_len", type=int,  default=500)
    parser.add_argument("--eval_len",  type=int,  default=2000)
    parser.add_argument("--out_dir",   type=str,  default="results")
    parser.add_argument("--verbose",   action="store_true")
    args = parser.parse_args()

    print(f"[Synthetic HR-MDP] device={DEVICE}  "
          f"p_switch={args.p_switch}  seed={args.seed}  epochs={args.epochs}")

    result = run_single_experiment(
        p_switch=args.p_switch, seed=args.seed,
        epochs=args.epochs,
        train_episode_len=args.train_len,
        eval_episode_len=args.eval_len,
        verbose=args.verbose,
    )

    suffix      = f"p{args.p_switch:.3f}_seed{args.seed}"
    json_path   = os.path.join(args.out_dir, f"experiment_{suffix}.json")
    _save_result(result, json_path)
    plot_single_experiment(result, args.out_dir)

    print("\n" + "=" * 60)
    print(f"  Final log-wealth   (higher = better)")
    print("=" * 60)
    for v in ("oracle", "soft", "hard", "baseline"):
        print(f"  {v:<10s}: {result['final_log_wealth'][v]:+.4f}")
    print("\n" + "=" * 60)
    print(f"  Final regret vs oracle  (lower = better)")
    print("=" * 60)
    for v in ("baseline", "hard", "soft"):
        print(f"  {v:<10s}: {result['final_regret'][v]:+.4f}")
    print("=" * 60)
    print(f"\nSaved → {json_path}")
